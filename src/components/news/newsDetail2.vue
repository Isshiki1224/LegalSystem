            <span style="font-family:inherit;">2应急科学与工程知识体系框架</span>
          </h1>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">2.1应急科学与工程体系来龙去脉</span>
          </h1>
          <span
            style="font-family:inherit;">众所周知,应急管理古已有之,但国内诸多专家学者聚焦应急管理却是从2003年非典期间开始，此后,国家逐步建立起了以”一案三制”为特征的我国应急管理体系。自2003年国内各界谈应急至今，应急落地是靠的什么？其实有的时候还是要靠应急产业。所以我们现在<span
              style="font-family:inherit;">关注</span>应急工作的落地，要有装备、有产品、有队伍等，更多关注点在应急产业问题上。最早提出应急管理这个词,更多的是从事公共管理、危机管理、自然灾害的学者专家倡导的。但事实上，我国生产安全领域也早已触及这方面实际工作，只不过没有明确提出应急管理这个术语。</span><span
            style="font-family:inherit;">我们会发现，应急工作最终会做成装备、做成产业，能摸得着，看得见。但是会发现，我们最早的应急管理工作出现了很多的问题，一方面搞应急管理的人，有时候很难驾驭搞应急工程技术的人，因为不擅长或者不懂工程技术，甚至搞工程技术的也不买搞管理的帐。另外一个很有意思的问题，虽然现在应急产业喊得这么响，安全生产的人在呼吁安全产业，做公共危机的人也在呼吁应急产业，搞自然灾害的人也在推动应急产业，但是各做各的，从应急资源整体综合利用角度,这就是个很麻烦的事。</span><span
            style="font-family:inherit;">应急管理部成立后，未来我国应急工作一个关键问题是应急队伍的建设，因为队伍建设需要配套措施，如装备、协调管理等。然而队伍的建设其实涉及到人才的问题，需要专业学科来支撑，光靠短期的培训，队伍很难将应急专业知识系统化，所以必须要加强应急人才的学历教育，那就需要学科支撑，但是现在我们教育部专业目录里没有这个专业名称。在这种情况下，能驾驭搞应急的软科学、应急的硬科学的这一种学科，我们能不能做？我们这几年在倡导应急科学与工程教育体系，它的内容就涉及到应急科学、应急技术、应急工程、应急产业和应急管理，后来有专家说应该再加一个应急文化。当然，我们倡导应急科学与工程教育体系不是空穴来风，是在15年应急管理(公共安全管理)本科专业教育办学基础上（河南理工大学早在2005年就率先在国内开展应急管理本科专业方向探索工作迄今），依据办学经验及结合实际工作提出来的。</span><span
            style="font-family:inherit;">从教育的方面来说，我们能不能建立应急科学与工程教育体系，即应急科学、应急技术、应急工程、应急产业及应急管理等，是能否满足社会需求及应急科学内在逻辑发展趋势需要。去年参加了教育部规建中心等组织的应急安全产教融合联盟筹备会，当时与许多专家学者交流时提出要设置应急工程专业，或者应急技术与管理、应急工程管理等，依靠单独的应急管理专业学科不能完全支撑实际应急工作。因为按照应急科学与工程教育体系思路，应急管理是应急科学、应急技术、应急工程、应急产业的一个综合体现，是统筹协调的问题。不知是否是我们这种观点或思想起了作用还是其他原因，今年教育部公布了特设的几所大学开设应急技术与管理本科专业，强化应急技术，当然管理也不可忽视。</span><span
            style="font-family:inherit;">我们现在的人才培养、学生就业、整体工业化进程，还需要工程技术的支撑。我们既然谈这个问题，就涉及到应急科学到底是怎么回事这一问题。我们这几年一直在应急科学与工程教育体系方面开展探索性研究，我们救援所先后在2017、2018年与《安全》杂志搞了两期应急专刊，邀请来自国家行政学院，人大、南大、中国地震局、中国安全生产科学研究院等国内应急领域的知名专家撰稿，两期应急专刊大概有40篇，《安全》杂志社免费出版，专家无偿编写。这些学术探索工作对应急科学与工程体系建设无疑是有一定推动作用的。正是在这个基础上，我们需要考虑应急科学设计到底怎么来做？既然讲科学应急，那么应急必须走向科学，支撑应急管理业务工作需要具体的科学体系，这就叫应急科学。我们尝试给出应急科学一个定义界定，，即应急科学是防范、消减、化转及控制一个系统遭受外界打击后，采取科学、技术、工程、产业及管理等方法，使该系统恢复到人们可接受状态(原状态或趋于新的平衡态)的科学。针对这个问题，有专家也指出，当一个研究方向可以称之为一门科学时，其最基本的问题是首先要回答两个问题，即它的数学基础是什么、数据来源是什么及相关模型构建等，把握系列基本问题是应急科学发展的关键。以应急科学与工程教育体系为支撑，我们也试图建立一个围绕应急科学与工程体系为拓展的知识体系整体框架，即应急科学与工程知识体系。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">2.2应急科学与工程知识体系如何实现整体分支设计</span>
          </h1>
          <span
            style="font-family:inherit;">应急科学与工程知识体系包括应急科学、应急技术、应急工程、应急产业及应急管理，当然应急文化也可以包括在内，这几个分支模块是该知识体系关键要素。许多灾害事故防控过程暴露了其中既存在许多基础理论问题，也存在更多的基础实验探索问题，基础理论问题及基础实验问题构成了应急科学问题如何可能这一命题。应急科学解决的是如何认识自然，发现自然界存在的客观规律的过程。仅仅认识自然还不行，如何改造自然，为人类服务，就涉及到应急技术问题，应急技术包括类型比较多，按照一般与特殊的分类标准可分为通用技术、专业（行业）技术两大类。应急科学、应急技术解决的是灾害事故防控基础研究、技术研发工作，如何把应急科学、应急技术的成果系统化，就必须要考虑工程集成问题，就涉及到应急工程设计，科学讲的是认识自然，技术是改造自然，工程是发现世界本没有的东西，比如我们现在理解的应急避难所、人防工程、民防工程等都是一个人造物的物品，是实体工程。既然有实体工程，自然有虚拟工程，如网络安全应急工程之类就属于应急虚拟工程范畴。还有应急产业设计，我们认为应急产业是整体的应急科学、应急技术及应急工程的落地终端，整个的应急管理业务要形成市场，对于国家来说需要形成一个应急的市场，那么应急产业必须要形成，这样才能使应急工作有足够的保障运行。现在应急产业的概念和应急产业的类型，工信部和国家发改委先后出了应急产业的目录，还有应急物资保障的目录。应急产业包括哪些内容？应急救援装备、应急物资、应急社会化服务（包括应急教育培训等），都有很多亟待探索的问题，每一块都需要我们去深入研究。还有应急管理的设计，应急管理是应急科学、技术、工程、产业的综合体现，应急科学、技术、工程、产业的内容做好了，应急管理能力水到渠成。所以按照这个逻辑，应急管理包括三个维度，一个是政府的应急管理，一个是企业的应急管理，一个是社会的应急管理。过去我们搞公共管理更多的是侧重政府的应急管理，搞安全生产侧重的是企业应急管理，现在讲社会救援力量是社会应急管理，包括公众老百姓的社会应急管理，或者叫公共安全应急管理。有专家提出还可以包括应急文化，事实上，应急文化体现在应急工作各个方面，实现无缝连接才是文化的本质。</span><span
            style="font-family:inherit;">应急科学与工程知识体系框架设计基于应急科学、应急技术、应急工程、应急产业及应急管理基础上拓展而设计，包括除前述几个关键要素外，还有应急哲学、应急文化、应急教育、应急队伍、应急能力、应急法规等，每个要素具体不在阐述。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">2.3应急科学与工程知识体系的现实价值</span>
          </h1>
          <span
            style="font-family:inherit;">未来按照应急科学与工程知识体系的设想，可做的工作很多，一个是应急科学的基础研究工作，我国在这块做得很少。不同领域应急人士从2003年开始聚焦应急研究，到目前为止十多年我们更多的是翻译介绍国外应急管理知识的普及工作，今后要开展的一个重要工作是，挖掘具有本土文化特点的应急科学理论、探索应急工程实践等系列工作。</span><span
            style="font-family:inherit;">应急科学不仅仅是应急科学的理论，还有应急科学的实验平台。我们现在实验测试是很少的。在应急这块必须要加大基础的实验测试工作，这是有价值的。没有数据，很多工作都很难开展。说到工程体系的城市应急、乡村应急，现在焦点都在城市安全、城市应急这块，乡村未来是怎么样？能不能做？怎么来提前做这个事？这都是值得深入思考的问题，需要我们来研究。另外还有应急产业体系，一个是应急装备的研发，包括物资等，我们国家都有目录，这个目录我们今后随着对应急产业概念认知深入、应急产业体系不断完善，有没有必要进行调整？应急社会化服务未来肯定是一个趋势，社会救援力量参与进来是很有意义的，尤其应急教育培训这块到底未来如何搞，尤其应急职业化资质认证这些工作非常迫切。可以看出，灾害事故频发，我们一直在强调科学应急，如何实现科学应急，如何给科学应急做好支撑工作？解决这个问题，就是要考虑从科学应急到应急科学的现实命题问题。</span><span
            style="font-family:inherit;">据此，应急科学就具有了顶天立地特征，即实现国家安全利益（即应急科学基础研究）与人民群众安全获得感（即应急科普落地生根）辩证统一，顶天立地特征将有助于国家整体应急能力提升，增进人民群众安全福祉，将成为未来我国应急管理事业重要发展趋势。在这个认识意义上，应急科学与工程知识体系的对于推动国家十四五应急体系规划编制设计就具有了一定学术理论支撑价值。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3国家“十四五”应急体系规划设计框架</span>
          </h1>
          <span
            style="font-family:inherit;">应急体系规划一般要有个编制说明，说明该规划编制依据的指导思想、政策法规以及现实经济社会发展情况等内容。然后在此基础上考虑后续具体内容。主要思路及内容概括一并如下：</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.1规划背景</span>
          </h1>
          <span
            style="font-family:inherit;">规划背景主旨在于遵循十九大报告，落实防灾减灾救灾的要求，摸清国家“十一五”、“十二五”、“十三五”应急规划建设现状及问题,承前启后、查遗补漏、补充建设等。可以从国家“十三五”期间建设成效及面临形势等方面进行表述。具体操作实施可以从宏观、微观两个层面开展此项工作，即宏观层面，从国家各职能部门等应急体系规划建设等角度梳理国家十三五应急体系规划建设现状；微观层面，对地方省市等各职能部门对应急体系规划等现状、问题进行调研。经过多种方式摸清国家十三五应急规划建设现状、问题后，针对十三五规划中，有关突发事件四大类灾害重点量化指标完成程度进行量化或定性表述，可以表格、文字等形式阐释。针对十四五面临形势相关的关键指标，可以文字形式重点阐释。目的是为国家十四五应急规划编制提供基础数据资料。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.2规划指导思想、建设原则及目标</span>
          </h1>
          <span
            style="font-family:inherit;">这部分要考虑三个方面内容，即规划指导思想、建设原则及建设目标。对于指导思想主要考虑遵循十九大报告，落实防灾减灾救灾的要求；国家“十一五”、“十二五”、“十三五”应急规划建设内容,总体国家安全观思想指导下，国家“一带一路”、“实施健康中国战略”、人类命运共同体等一系列重大决策都对应急管理事业提出了挑战和机遇，也给国家十四五应急体系规划编制设计指明了方向和工作重点。全面贯彻落实党的相关政策法规等思想指导下，进行综合考虑。</span><span
            style="font-family:inherit;">对于建设原则，考虑在继承十三五应急规划原则基础上，修改为：源头治理、关口前移；统筹规划，突出重点；政府主导，多方协同；应急服务，法治保障等建设原则。</span><span
            style="font-family:inherit;">对于建设目标，主要是搞清楚国家十四五综合应急体系规划需求目标。解决这个问题的具体实施措施包括宏观、微观两个方面，即宏观层面，国家十四五应急体系规划建设面临的形势和挑战。从国家及国家各职能部门对应急工作定位、突发事件发生态势等角度系统分析国家十四五应急体系规划建设面临的形势和挑战，提出宏观需求目标。微观层面，要把握两点，即第一，对地方省市等各职能部门对应急体系规划需求目标进行调研。第二，结合国家及各职能相关政策，结合地方省市整体实际情况，对国家应急体系规划需求目标进行调研。在宏观、微观基础上，结合国家十四五应急体系规划建设面临的形势和挑战，以及综合国家及国家各职能部门、地方省市整体实际情况，进一步明确国家十四五应急体系规划需求目标。需求目标包括总体目标和分类目标，<em>本研究给出总体目标:到2025年，建成统一指挥、专常兼备、反应灵敏、上下联动、平战结合的应急管理体制，形成统一领导、权责一致、权威高效的应急管理体系。应急科学基础研发能力持续提升，应急关键技术能力显著增强，应急工程体系进一步加强</em>，应急产业体系进一步完善，应急综合管理能力全面加强，社会协同应对能力进一步提升，涉外应急能力进一步巩固，生命安全保护能力再上新台阶。</span><span
            style="font-family:inherit;">与总体目标和主要任务相对应，按照相关性、针对性、综合性、可实现、可分解落实、定性与定量相结合的原则，确定“十四五”期间各重点建设领域的规划分类目标。分类目标与具体建设任务相对应，分类目标主要是确定“十四五”期间各重点建设领域的规划分类目标。本研究给出具体分类目标：包括14个方面，即应急哲学理念变革提升体系建设、应急文化体系建设、建立健全应急队伍体系、应急科学与工程整体能力建设、应急科学基础体系建设、应急关键技术体系建设、应急工程系统体系建设、应急产业体系建设、应急综合管理体系建设、进一步完善生命安全保护体系建设、应急管理法规体系建设、进一步加强处置灾种体系规律建设、建立健全应急信息系统平台建设等。此外，分类目标中针对四大类突发事件相关关键指标，可以量化或定性形式以表格形式阐述。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3主要建设任务</span>
          </h1>
          <span
            style="font-family:inherit;">主要建设任务包括14个方面，即应急哲学理念变革提升体系建设、应急文化体系建设、建立健全应急队伍体系、应急科学与工程整体能力建设、应急科学基础体系建设、应急关键技术体系建设、应急工程系统体系建设、应急产业体系建设、应急综合管理体系建设、进一步完善生命安全保护体系建设、应急管理法规体系建设、进一步加强处置灾种体系规律建设、建立健全应急信息系统建设等。本研究主要针对14个方面建设任务进行概括性阐释。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.1应急哲学理念变革提升体系</span>
          </h1>
          <span style="font-family:inherit;">加强应急哲学理念变革体系研究，推动并转变应急服务理念体系建立。</span><span
            style="font-family:inherit;">建议相关部门牵头，联合有一定应急教育基础的科研院校、企事业单位设置专项开展应急哲学思想体系、应急服务理念工作模式及试点工作。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.2</span><span style="font-family:inherit;">应急文化体系建设</span>
          </h1>
          <span style="font-family:inherit;">应急文化体系建设包括应急科学文化、应急技术文化、应急工程文化、应急产业文化及应急管理文化等。建议依托</span><span
            style="font-family:inherit;"><span style="font-family:inherit;">应急</span><span
              style="font-family:inherit;">、文化安全相关科研院校、企事业单位联合开展应急文化理论体系探索及工程示范研究。</span></span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.3全面强化应急教育体系</span>
          </h1>
          <span style="font-family:inherit;">应急教育体系包括基础教育、培训教育、学历教育、知识更新教育及科普教育等。</span><span
            style="font-family:inherit;">建议多个部门联合攻关，全面加强应急基础教育；应急培训教育模式、资质标准；建议依托有一定办学基础的应急科研院校、企事业单位联合组建应急教育类大学、高职及中职办学机构，探索应急科学与工程一级学科理论体系及办学试点工作；开展应急知识更新教育线上线下学习平台建设及示范点工作；组建应急科普基础研究科研机构，开展科学有效的应急科普基础理论、知识传播工作，探索精准的应急科普知识生产渠道，挖掘应急科普走入基层群众的有效路径及模式。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.4建立健全应急队伍体系</span>
          </h1>
          <span style="font-family:inherit;">应急队伍体系主要针对不同类型应急队伍来说，这里将应急队伍类型划分为应急队伍、救援队伍及立体化应急（救援）队伍等。</span><span
            style="font-family:inherit;">建议加强队伍基础理论、实训科目及配套实训平台建设工作。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.5</span><span style="font-family:inherit;">应急科学与工程整体能力建设</span>
          </h1>
          <span
            style="font-family:inherit;">应急科学与工程整体能力包括应急科学能力、应急技术能力、应急工程能力、应急产业能力及应急管理能力等。五种应急能力与突发事件发生演变过程而采取的应急四阶段措施，即突发事件预防准备、监控预警、处置救援及恢复重建，其所形成的能力，即预防与准备能力、监控与预警能力、处置与救援能力、恢复重建能力，构成了横纵矩阵，体现了应急综合管理业务能力。</span><span
            style="font-family:inherit;">在总体国家安全观指导下,建议加强应急科学基础研究工作、建立健全公共安全信息化网络化智能化、加强应急资源共享信息化平台建设、建立应急指挥与预案演习理论、标准及实训实验基地等基础上，加强国际合作，探索集物理世界、网络世界与内心世界于一体的具有中国特色的应急科学与工程整体响应能力体系化建设工作等。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.6应急科学基础体系建设</span>
          </h1>
          <span style="font-family:inherit;">应急科学基础体系包括基础理论体系、基础实验体系以及其它相关支撑科学。</span><span
            style="font-family:inherit;">建议相关部门牵头，设置国家层面应急科学基础研究科研机构；鼓励科研院校开展应急科学基础研究办学试点和相关研究方向，并出台配套政策；设置重大专项开展应急科学基础体系研究。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.7应急关键技术体系建设</span>
          </h1>
          <span style="font-family:inherit;">应急关键技术体系包括通用技术、应急专业（行业）技术两大类,从而形成面向实践的应急技能体系。</span><span
            style="font-family:inherit;">建议相关应急企业牵头，联合相关科研院所，共同组建国家应急关键技术研发机构，开展针对重大灾害的应急关键技术研究，并出台配套政策；鼓励中小企业参与常见频发灾害应急关键技术研究，并出台配套政策。考虑云计算、大数据、物联网及人工智能等在应急关键技术体系建设中运用。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.8应急工程系统体系建设</span>
          </h1>
          <span style="font-family:inherit;">应急工程系统体系包括应急实体工程、应急虚拟工程两个体系建设工作。</span><span
            style="font-family:inherit;">建议政府牵头，联合相关企业及科研院所，开展重大应急工程原理、工程伦理基础研究及若干试点工作，并出台配套政策。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.9.应急产业体系建设</span>
          </h1>
          <span
            style="font-family:inherit;">目前对应急产业概念、类型及功能定位等存在很大认识盲区，需要深入探索，将应急产业体系包括应急装备产业、应急保障物资产业、应急服务产业及生物搜救产业等。</span><span
            style="font-family:inherit;">建议进一步开展应急产业概念、类型及功能定位基础研究，同时考虑适时将生物搜救产业纳入应急产业目录中，并出台配套政策。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.10.应急综合管理体系建设</span>
          </h1>
          <span style="font-family:inherit;">应急综合管理体系包括政府（自然灾害）应急管理、企业（生产事故）应急管理、社会（综合灾害、紧急情况）应急管理等。</span><span
            style="font-family:inherit;">建议进一步转变应急工作理念，由管控型到服务型转变；探索政府应急服务、企业属地治理及公众社会动员三种不同层级、导向的防灾减灾救灾的组织体系和方法。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.11进一步完善生命安全保护体系建设</span>
          </h1>
          <span
            style="font-family:inherit;">未来安全工作不仅限于国土范围的陆海空尺度，还要考虑全人类及太空安全尺度，既着眼国家安全利益与人民安全利益相统一，也要综合考虑人类命运与太空生态空间相统一。这样认识理念上，应急体系规划工作中的生命安全保护体系建设囊括国家安全利益、人民安全利益、涉外安全利益、人类命运共同体、太空安全等。</span><span
            style="font-family:inherit;">建议进一步探索以人为本、生命至上、科学应急的生命安全保护体系建设工程。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.12</span><span style="font-family:inherit;">应急法律法规体系建设</span>
          </h1>
          <span style="font-family:inherit;">应急法律法规体系包括未来“1+4”应急管理法律法规体系建设以及其他相关政策建设。</span><span
            style="font-family:inherit;">建议进一步探索以人为本、生命至上、科学应急的应急法律法规体系建设工程。开展保险业介入应急工作的配套法律法规体系研究及试点工作。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.13</span><span style="font-family:inherit;">进一步加强处置灾种体系规律建设</span>
          </h1>
          <span
            style="font-family:inherit;">突发事件应对法将处置灾种划分为四类，即自然灾害、生产事故、突发卫生事件及社会安全事件。研究国内外突发事件不同角度、不同标准分类；研究中医学理论的将人、自然与社会的三重关系划分，即人与自然、人与人、人与自身等，依据三重关系划分突发事件类型：自然灾害、人为灾害、身心灾害，研究其基本科学规律，进一步丰富并推动处置灾种体系规律建设。建议进一步探索有利于科学应急的灾种体系规律建设工作。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.3.14建立健全应急信息系统平台建设</span>
          </h1>
          <span
            style="font-family:inherit;">应急管理业务工作涉及安全生产、自然灾害等不同行业领域,而不同行业领域信息完善和融合，对预防和应急工作的作用是巨大的,在应急科学与工程整体能力支撑下,亟待建立不同行业领域的具有信息监控预警等功能集成的应急信息系统平台。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4重点建设项目</span>
          </h1>
          <span
            style="font-family:inherit;">“十四五”期间，在整合现有应急资源的基础上，以重大应急科学问题及应急工程技术为突破，着重强化应急科学与工程整体能力，提升应急综合管理能力，从实际出发，重点抓好8项整合性、全局性的重点建设项目。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4.1应急科学基础实验体系整体设计平台</span>
          </h1>
          <span
            style="font-family:inherit;">依托已有一定办学、科研基础的高等院校、科研院所及企事业单位联合研发，建立健全具有本土文化特征的应急科学基础理论与基础实验平台，对于支撑应急管理业务工作非常急迫。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4.2</span><span style="font-family:inherit;">应急共性关键技术体系开发</span>
          </h1>
          <span style="font-family:inherit;">&nbsp;&nbsp;
            &nbsp;依托已有一定办学、科研基础的高等院校、科研院所及企事业单位联合研发灾害事故（安全生产、防灾减灾、消防等）应急共性关键技术，尤其救援安全保障措施要作为重点攻关。研究云计算、大数据、物联网及人工智能等信息技术应急共性关键技术开发中的应用及示范工程。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4.3应急工程（训练基地）规范化标准化</span>
          </h1>
          <span style="font-family:inherit;">依托已有一定办学、科研基础的科研院校及若干救援训练基地联合开展应急工程（训练基地）规范化标准化课题攻关及示范工程。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4.4</span><span style="font-family:inherit;">应急产业功能演变基本规律及示范工程</span>
          </h1>
          <span
            style="font-family:inherit;">依托不同规模层次的应急企业，联合相关的科研院校开展应急产业概念界定、类型划分、功能定位等基础研究，进一步深入探索应急产业示范基地建设理论与实践工程。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4.5应急管理综合统筹优化智能信息化设计</span>
          </h1>
          <span
            style="font-family:inherit;">依托相关机构开发并建立能支撑国家应急工作的应急资源信息化、网络化、智能化兼具安全性的共享平台，体现业务连续性思想，实现平台基础设计、工程化转化的整体连续性运维。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4.6组建中国应急科技大学建设工程</span>
          </h1>
          <span
            style="font-family:inherit;">依托有一定办学基础的高等院校、科研院所及大型企业联合组建中国应急科技大学，中国应急科技大学设置主校区、布局全国的若干分校区。该大学办学目的是攻关应急科学基础、应急关键技术瓶颈等。办学定位是应急学历教育、培训教育、科普教育等，目的培养专业化、系统化的应急人才队伍。组建中国应急科技大学非常重要目标是推动中国应急科普教育事业。通过中国应急科技大学建设工程，带动一批服务基层，深耕基础，面向灾害实际的应急中职、高职、本科及研究生办学试点，培养一定体量的基层应急响应队、理论与实践兼备的现场应急指挥官队伍、引领基础前沿的研发人才。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4.7基层专业救援队伍建设工程</span>
          </h1>
          <span style="font-family:inherit;">应急工作落地在基层，基层应急工作关键要素之一是救援队伍建设问题，大力开展基层不同</span><span
            style="font-family:inherit;">救援队伍组建、运维等工作，包括队伍训练基地、训练科目、师资队伍、运维机制等系统性工作研究。依托区域应急救援中心工程等不同类型层次基地，建设一批基层专兼备的救援队伍。</span>
          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">3.4.8应急设备和救灾物资储备设施建设工程</span>
          </h1>
          <span style="font-family:inherit;">建设一批从国家、省、市、区县、街道乡镇等不同层级的兼具避难避灾功能的应急综合物资与装备储备库示范工程。</span>
