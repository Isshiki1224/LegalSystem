          </h1>
          <span
            style="font-family:inherit;">十三五应急体系规划工作已接近尾声，十四五应急体系规划工作在国家层面、各省市层面正陆续启动，国家十四五应急体系规划设计对于全国未来五年应急管理工作、地方省市规划工作及具体实施具有重要引领性和指导性。在全面贯彻十九大报告，落实防灾减灾救灾的要求，在“十一五”、“十二五”、“十三五”规划建设的基础上,在总体国家安全观思想指导下，国家“一带一路”、“实施健康中国战略”、人类命运共同体等一系列重大决策都对应急管理事业提出了挑战和机遇，也给国家十四五应急体系规划编制设计指明了方向和工作重点。在此现实背景下，国家十四五应急体系规划到底基于何种指导思想、基本原则、达到什么需求目标、该如何设计、考虑哪些重要工作内容、具体重点落地工程是哪些方面、需要做好哪些保障措施等都是需要全盘考虑的。据此，基于国家应急管理相关政策，结合国内外突发事件应急管理理论前沿及突发事件发展面临形势，本研究基于应急科学与工程知识体系角度，试图对国家十四五应急体系规划建设进行学术研究，希冀此探索性工作对于国家十四五应急体系规划编制设计有所裨益，个中不成熟观点，也请方家不吝赐教。</span>
          <h1
