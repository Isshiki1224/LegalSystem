          <h1
            style="font-family:Helvetica, &quot;font-size:24px;color:#3E3E3E;text-align:justify;background-color:#F7F7F7;">
            <span style="font-family:inherit;">4结论</span>
          </h1>
          <span
            style="font-family:inherit;">（1）应急科学与工程知识体系对于应急体系规划设计具有一定学理支撑。阐明应急科学与工程知识体系框架，包括其来龙去脉、框架设计思路及现实应用价值。</span><span
            style="font-family:inherit;">（2）依据应急科学与工程知识体系主体思路，依照规划设计框架，提出国家“十四五”应急体系规划设计若干思考，包括14项主要建设任务，即应急文化体系建设、建立健全应急队伍体系、应急科学基础体系建设、进一步完善生命安全保护体系建设、应急管理法规体系建设等。针对14项主要建设任务具体落地实施，提出了8项重点建设工程，即应急科学基础实验体系整体设计平台、应急管理综合统筹优化智能信息化设计、组建中国应急科技大学建设工程等。</span><span
